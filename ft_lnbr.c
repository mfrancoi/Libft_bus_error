int		ft_lnbr(char const *s, char c)
{
	static int	i;
	int			y;

	y = 0;
	while (s[i] == c && s[i])
		i++;
	while (s[i] && s[i] != c)
	{
		i++;
		y++;
	}
	return (y);
}
